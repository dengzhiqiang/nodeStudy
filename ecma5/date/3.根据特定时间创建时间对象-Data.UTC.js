// 根据特定时间创建时间对象,必须在时间对象中传入时间的毫秒数
// 毫秒数是从1970.1.1到特定的时间

// 年月日时分秒以及毫秒数


//2017-11-27T10:01:01.000Z  10代表的11月 月份是从0开始的
var data = new Date(Date.UTC(2017, 10, 27, 10, 1, 1));
console.log(data);


// 会自动调用UTC方法
//2017-11-27T02:01:01.000Z
var data2 = new Date(2017, 10, 27, 10, 1, 1);
console.log(data2);